function updateState()
	

	if (memory.readbyte(0x02022B4C)==0) then
		state="overworld"
	elseif(memory.readbyte(0x02022B4C)==4) then
		state="wild"
	elseif(memory.readbyte(0x02022B4C)==12) then
		state="battle"
	end
		
end

function draw(arr,X,Y)
	for j=1,#arr do
	for i=1,#(arr[1]) do
		
			
			gui.pixel(X+i, Y+j, arr[j][i])

		end
	end
end

